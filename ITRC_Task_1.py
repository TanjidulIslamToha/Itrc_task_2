# Prompt the user to enter a number and convert it to a float
x = float(input("Enter a number: "))

# Double the entered number
doubled = x * 2

# Add 10 to the doubled number
added = doubled + 10

# Divide the result by 2
divided = added / 2 

# Subtract the original number from the result
result = divided - x 

# Print the final result
print("Result: ", result)